public void armarCabecera(){
ArrayList<Object>col=new ArrayList();
col.add("ad materia");
col.add("nombre");
col.add("anio");
    for (Object colu : col) {
        modelo.addColumn(colu);
        
    }
    jTablaMaterias.setModel(modelo);

}
